function makePDFviews(orig_dir,output_dir)



proj_name = 'DeepWholeBrain';
expr_name = 'SLANT';

spider_name = 'DeepWholeBrain';

% load('braincolor_cmap.mat', 'cmap_orig_vals');
% load('braincolor_cmap.mat', 'cmap_vals');
cmap_orig_vals = get_cmap();
sublist = dir([orig_dir filesep '*.nii.gz']);

for i = 1:length(sublist)
    subFile = sublist(i).name;
    subName = get_basename(subFile);
    rawfile = [output_dir filesep subName filesep 'target_processed.nii.gz'];
    segfile = [output_dir filesep subName filesep 'working_dir' filesep 'all_piece' filesep ...
        '5000_fusion27_mv' filesep 'test_out' filesep 'seg_output' filesep 'epoch_0027' filesep ...
        'seg_orig_final' filesep 'target_processed_seg.nii.gz'];
    
    temp_dir = [output_dir filesep subName filesep 'temp'];
    if ~isdir(temp_dir);mkdir(temp_dir);end;
    subj_name = subName;
    pdf_dir = [output_dir filesep 'FinalPDF'];
    if ~isdir(pdf_dir);mkdir(pdf_dir);end;
    pdfname = [pdf_dir filesep sprintf('%s_result.pdf', subName)];
    scan_name = sprintf('Scan%d',i);
    generate_brianseg_pdf_xiong(rawfile, segfile, pdfname, cmap_orig_vals,...
        temp_dir,proj_name, subj_name, expr_name, scan_name, spider_name, i, length(sublist))

end

end

